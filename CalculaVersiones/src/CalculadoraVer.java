
public class CalculadoraVer 
{

	int num1, num2;

	public CalculadoraVer(int num1, int num2) 
	{
		this.num1 = num1;
		this.num2 = num2;
	}
	
	public static int suma(int num1, int num2)
	{
		int result=num1+num2;
		return result;
	}
	
	public static int resta(int num1, int num2)
	{
		int result=num1-num2;
		return result;
	}
	
	
	
}
